static const uint8_t* glyph_for(char c) {
    uint8_t u = (uint8_t)c;
    if (u < FIRST_GLYPH || u > LAST_GLYPH) return glyph_missing;
    return font8x16[u - FIRST_GLYPH];
}

void font_draw_char(int32_t x, int32_t y, char c,
                    fb_color fg, fb_color bg, bool bg_opaque) {
    const uint8_t* g = glyph_for(c);
    for (int32_t row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = g[row];
        for (int32_t col = 0; col < FONT_WIDTH; col++) {
            bool lit = bits & (0x80 >> col);
            if (lit) {
                fb_put_pixel(x + col, y + row, fg);
            } else if (bg_opaque) {
                fb_put_pixel(x + col, y + row, bg);
            }
        }
    }
}

void font_draw_n(int32_t x, int32_t y, const char* s, uint32_t n,
                 fb_color fg, fb_color bg, bool bg_opaque) {
    int32_t cx = x;
    for (uint32_t i = 0; i < n; i++) {
        char c = s[i];
        if (c == '\0') return;
        if (c == '\n') return; // newlines are caller's problem here
        font_draw_char(cx, y, c, fg, bg, bg_opaque);
        cx += FONT_WIDTH;
    }
}

static void font_draw_char_scaled(int32_t x, int32_t y, char c,
                                  fb_color fg, int scale) {
    const uint8_t* g = glyph_for(c);
    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t bits = g[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            if (bits & (0x80 >> col)) {
                fb_fill_rect(x + col * scale, y + row * scale,
                             scale, scale, fg);
            }
        }
    }
}

void font_draw_string_scaled(int32_t x, int32_t y, const char* s,
                             fb_color fg, int scale) {
    int32_t cx = x;
    for (uint32_t i = 0; s[i]; i++) {
        if (s[i] == '\n') {
            cx = x;
            y += FONT_HEIGHT * scale;
            continue;
        }
        font_draw_char_scaled(cx, y, s[i], fg, scale);
        cx += FONT_WIDTH * scale;
    }
}

int32_t font_draw_string(int32_t x, int32_t y, const char* s,
                         fb_color fg, fb_color bg, bool bg_opaque) {
    int32_t cx = x;
    int32_t cy = y;
    for (uint32_t i = 0; s[i]; i++) {
        char c = s[i];
        if (c == '\n') {
            cx = x;
            cy += FONT_HEIGHT;
            continue;
        }
        font_draw_char(cx, cy, c, fg, bg, bg_opaque);
        cx += FONT_WIDTH;
    }
    return cy;
}
